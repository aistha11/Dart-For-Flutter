//Control Flow Statements
/*
 * If and Else
 * Conditional  Statements
 * Switch Case Statement
 * */
void main ()
{
  
  //Switch Case Statement
  String grade = "A";
  switch (grade){
    case 'A':
      print("Excellent grade of A");
      break;
    case 'B':
      print("Very Good");
      break;
    case 'C':
      print("Good but Work hard");
      break;
    case 'F':
      print("Sorry You have failed");
      break;
    default:
      print("Invalid grade");
  }  
}
