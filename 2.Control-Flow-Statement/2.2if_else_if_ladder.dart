//Control Flow Statements
/*
 * If and Else
 * Conditional  Statements
 * Switch Case Statement
 * */
void main ()
{
  
  
  //If else If ladder statement
  var age = 101;
  
  if(age >=0 && age <= 16){
    print("Child");
  } else if(age >=16 && age <= 40){
    print("Young");
  } else if(age >=40 && age <= 100){
    print("Old");
  } else if(age >=100 && age <= 120){
    print("Going to Die");
  }else {
    print("You are dead");
  }
    
}
